export interface IdToken {
  id: number;
  token: string;
}

export type userData = IdToken | undefined;

export function useIdToken(): userData {
  if (typeof window !== "undefined") {
    const IdToken: any = sessionStorage.getItem("IdToken");
    return JSON.parse(IdToken);
  }
}
